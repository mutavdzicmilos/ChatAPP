    private void all(String str) {
        tLast.setText(str);
        strings.add(str);
        if (tFull.getText().equals("")) {
            tFull.setText(tFull.getText() + str);
        } else {
            tFull.setText(tFull.getText() + "\n" + str);

        }
    }

    private void cekanje() {
        Thread thread = new Thread(new Runnable() {
            @Override
            public void run() {
                try {
                    bSend.setEnabled(false);
                    tMessage.setEditable(false);
                    String str = "";

                    br = new BufferedReader(new InputStreamReader(socket.getInputStream()));
                    str = br.readLine();
                    all(str);
                    if (str.endsWith("end")) {
                        socket.close();
                        return;
                    }
                    bSend.setEnabled(true);
                    tMessage.setEditable(true);
                } catch (IOException ex) {
                    System.out.println("Connection is closed");
                }
            }

        });
        thread.start();
    }

