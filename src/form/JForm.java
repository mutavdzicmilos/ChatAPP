    private void bSendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bSendActionPerformed
        try {
            // TODO add your handling code here:
            String str = role + tMessage.getText().trim();
            tLast.setText(str);
            pr = new PrintWriter(socket.getOutputStream());
            pr.println(str);
            pr.flush();
            if (str.endsWith("end")) {
                socket.close();
            }
            all(str);
            tMessage.setText("");
            
            cekanje();

        } catch (IOException ex) {
            Logger.getLogger(JForm.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_bSendActionPerformed

